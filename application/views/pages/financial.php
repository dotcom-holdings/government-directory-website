				<p><a name="Awarding" id="Awarding"></a><strong>AWARDING OF TENDERS OR BIDS</strong></p>

				<p>The document <a href="/assets/departments/economic-development-tourism/wcbd_6.1_preference_points.pdf" target="_blank">SBD/WCBD6.1</a> explains how tenders or bids are decided. WCBD says:</p>

				<ul><li>The bidder obtaining the highest number of points will be awarded the contract.</li>
					<li>Preference points shall be calculated after prices have been brought to a comparative basis.</li>
					<li>Points scored will be rounded off to two decimal places.</li>
					<li>In the event of equal points scored, the bid will be awarded to the bidder scoring the highest number of points for specified goals.</li>
				</ul>
				What this means in practice is the following. After the closing date, an elementary check is done on all the tenders submitted to see if they comply with the formal requirements. For example, if you have not indicated a price, your tender will be disqualified.

				<p>Smaller mistakes or omissions may or may not disqualify you from the tender process; this is a question of interpretation of the tender rules. For example, if the rules say "may disqualify" rather than "must disqualify" for a specific mistake, it is up to the official doing the check to decide whether the mistake is important enough to this tender to disqualify you.</p>

				<p>The next phase looks at compliance of the product or services with the specifications and price. Those which do not comply with the specifications are removed from the list, while all the tenders which comply with the specifications are listed in order of price. Those that fall in the lowest price group are then considered in a lowest price tender list.</p>

				<p>It is in this phase that the preference points come into play. All the preference points claimed by those on the list of lowest price tenders are first verified. Then the formula is applied to determine who of those on the lowest price list with verifiable points come out with the best result on points, and therefore who should be awarded the contract.</p>

				<p>In other words, preference points only come into play after the most expensive tenders have first been excluded. This is to ensure that the most expensive options do not win solely on points, and also to speed up the process, as only those on the lowest price list have their preference points verified.</p>

