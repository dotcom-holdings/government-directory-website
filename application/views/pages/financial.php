				<p><a name="how to" id="how to"></a><strong>HOW TO COMPLETE A TENDER OR BID</strong></p>

				<p>Each tender or bid advert indicates where you can collect the documents you will need to fill in to submit your tender, and where they should be submitted. The advert also indicates a closing date. This is a very firm deadline; no late tenders can be accepted.</p>

				<p><strong>NB:</strong> Look out for any compulsory site meetings or any other special conditions of contract as non-compliance can disqualify you.</p>

				<p>Tenders or bids have to be in writing. Each tender has a number of associated <a href="#forms">forms</a> which must accompany the tender you submit. The specific forms you require for your tender should be listed in the tender documentation. You should consider very carefully how you fill in these forms. Get <a href="/tenders/advice_centres">advice</a> if you are unsure of anything.</p>
			</td>
