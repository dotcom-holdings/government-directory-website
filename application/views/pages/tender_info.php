<div class="subheading"><strong>WHAT IS A TENDER OR BID?</strong></div>

<p>&nbsp;</p>

<p>&nbsp;
	</p><p>A tender is an offer to do work or supply goods at a fixed price. Getting goods or services is also known as "procurement". Since January 2004, government began referring to tenders as "bids". When government "puts out a tender" or "invites bids", this means government asks the public for price offers to do work or supply goods. Government then assesses who to choose based on the prices offered and the nature of the person or company making the tender.</p>
<p></p>

<p>&nbsp;</p>

<p>The tender or bid process is designed to ensure that the work to be done for government is given out in a fair way. There are a number of policies (known as "procurement policies") with special mention of the <a href="https://www.westerncape.gov.za/Text/2010/7/preferential_procurement_policy_framework_act_5_of_2000.pdf" target="_blank">Preferential Procurement Policy Framework Act (PPPFA)</a> that guides government on how to make decisions on which tender to accept. Although price is very important in the decision on which tender or bid to accept, it is not the only factor taken into account.</p>

<p>Once government accepts a tender, it is binding on both parties. This means that the person or company that won the tender has to provide the goods or services in the manner agreed to and at the price offered, and government must pay the agreed price at the agreed time. In other words, once accepted, a tender is a binding contract.</p>

