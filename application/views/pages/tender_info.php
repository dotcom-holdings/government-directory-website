<div class="subheading"><strong>WESTERN CAPE SUPPLIER DATABASE</strong></div>

<p>&nbsp;</p>

<p>&nbsp;
	</p><p>The Western Cape Provincial Government also has a contract with Quadrem/TradeWorld to populate and maintain a supplier database, called the Western Cape Supplier Database (WCSD) which can be requested by e-mail: <a href="mailto:supplierdatabase@quadrem.com">supplierdatabase@quadrem.com</a>. This <a href="http://pawc1i.epsi.co.za/login/login.asp" target="_blank">database</a> is shared with the City of Cape Town.</p>
<p></p>

<p>&nbsp;</p>

<p>Registration on the <a href="http://www.tradeworld.net" target="_blank">Western Cape Supplier Database</a> (accessed through Tradeworld) is without charge. At the moment, being on the database does NOT mean you will have tenders (RFQs) sent to you. It is a means of helping to verify who you are, speeds up the whole tender process, and is required in order for you to be compliant to do business with the entities that own and share the database.</p>

<p>This shared database serves as the registration base for all suppliers wishing to conduct business with both the City of Cape Town and Provincial Government of the Western Cape.</p>

<p>That is, suppliers register ONCE on this database to do business with both parties, even if previously registered on one of their existing databases. If you are already registered with the City of Cape Town, you do not need to re-register. In order to be registered and approved on the Western Cape Supplier Database free of charge, please collect a registration form from:</p>

