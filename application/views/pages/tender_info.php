<p><a name="05" id="05"></a></p>

<p><strong>Local Government Tenders</strong></p>

<p>The City of Cape Town advertises its tenders in Cape Town newspapers in their classifieds section on Fridays in the legal section. The municipalities may advertise in the Government Tender Bulletin (GTB) as well. The five district councils and 24 local councils in the Western Cape are required to advertise their tenders in local newspapers and in public places, such as libraries and council noticeboards. You can also <a href="http://www.salga.org.za/pages/Municipalities/Contact-Details/Western-Cape" target="_blank">contact a local council directly</a> to find out about local tenders or search for their listings on the <a href="http://www.capetown.gov.za/tenders/" target="_blank">City of Cape Town website</a>.</p>

