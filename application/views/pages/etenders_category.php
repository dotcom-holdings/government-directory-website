<p><u><strong>Tender Types:</strong></u></p>
<p>RFx is a term used to refer to a family of ‘Request For…’ documents used to solicit responses of various types from suppliers. The three documents used in this family include Request for Information (RFI), Request for Proposal (RFP) and Request for Quote. (RFQ)</p>
<p><em><strong>Request for Information (RFI)</strong></em><br>An RFI is generally used when the solution to a business problem is not immediately evident or clearly defined. The RFI is used to gather information, NOT to make a selection or an award. The SCM Unit works with the Customer to:</p>
<ul><li>clearly describe the problem</li>
<li>solicit external expertise regarding how to solve the problem,</li>
<li>study proposed solutions.</li>
</ul><p><em><strong>Request for Proposal (RFP) </strong></em><br>An RFP is used when the Customer understands the business problem and what’s needed to solve it, including specifications and procedures. Price is usually not the determining factor in the evaluation of an RFP. Factors such as quality, service, and reputation are also taken into consideration.</p>
<p><em><strong>Request for Quote (RFQ)</strong></em><br>An RFQ is generally used to obtain pricing, delivery information, terms and conditions from suppliers. In this case, requestors have a clear understanding of what they need, including requirements and specifications. To procure the exact product or service you need, the Customer provides the SCM Unit with as much information as possible, including complete specifications, quantities, and delivery schedule.</p>
